def create_working_paper(project_dir: Path, args) -> None:
    """创建底稿文件（基于模板，替换占位符）"""
    # 尝试从 skill assets 目录加载模板
    script_dir = Path(__file__).parent
    template_path = script_dir.parent / "assets" / "working-paper-template.md"

    if template_path.exists():
        template = template_path.read_text(encoding="utf-8")
    else:
        # 内嵌基础模板
        template = _embedded_template()

    # 替换占位符
    replacements = {
        "[PROJECT_NAME]": f"{sanitize_filename(args.target_company)}-DD",
        "[TARGET_COMPANY]": args.target_company,
        "[CLIENT_NAME]": args.client_name,
        "[BASE_DATE]": args.base_date,
        "[PURPOSE]": args.purpose,
        "[LAWYERS]": args.lawyers,
        "[LAW_FIRM]": args.law_firm,
        "[DATE]": str(date.today()),
    }

    for placeholder, value in replacements.items():
        template = template.replace(placeholder, value)

    (project_dir / "working-paper.md").write_text(template, encoding="utf-8")


def create_report_dir(project_dir: Path) -> None:
    """创建报告目录"""
    report_dir = project_dir / "report"
    report_dir.mkdir(exist_ok=True)
    readme = report_dir / "README.md"
    readme.write_text(
        "# 报告目录\n\n运行 `report` 模式后，生成的报告将保存在此目录。\n",
        encoding="utf-8",
    )


